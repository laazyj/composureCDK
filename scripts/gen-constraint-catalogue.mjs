#!/usr/bin/env node

/**
 * Generates `docs/constraints.md` — the single browsable index of every
 * AWS-property constraint in the library (see ADR-0010).
 *
 * Discoverability is a convention, not a runtime aggregate: each builder
 * package exposes its constraints through a uniform `constraints` namespace
 * (`constraints.validate.*` / `constraints.sanitize.*`), and there is no single
 * importable object listing them all. This script provides the browse-everything
 * view instead by importing each published package's built output and reading
 * its exported `constraints` namespace. Because it reads `dist`, run
 * `npm run build` first.
 *
 * - default: write `docs/constraints.md`.
 * - `--check`: regenerate in memory and exit non-zero if the committed file is
 *   stale. Wired into `npm run verify` so a new constraint can't merge without
 *   its catalogue row.
 *
 * Usage:
 *   node scripts/gen-constraint-catalogue.mjs
 *   node scripts/gen-constraint-catalogue.mjs --check
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { format, resolveConfig } from "prettier";

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const PACKAGES_DIR = join(REPO_ROOT, "packages");
const OUTPUT = join(REPO_ROOT, "docs", "constraints.md");

const MODES = ["validate", "sanitize"];

async function discover() {
  const entries = [];
  for (const dir of readdirSync(PACKAGES_DIR).sort()) {
    const manifestPath = join(PACKAGES_DIR, dir, "package.json");
    if (!existsSync(manifestPath)) continue;
    const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
    if (manifest.private) continue;

    const distIndex = join(PACKAGES_DIR, dir, "dist", "esm", "index.js");
    if (!existsSync(distIndex)) {
      throw new Error(
        `Missing build output for ${manifest.name} (${distIndex}). Run \`npm run build\` first.`,
      );
    }

    const namespace = (await import(pathToFileURL(distIndex).href)).constraints;
    if (namespace === undefined) continue;

    // Group the validate/sanitize references by constraint name so a constraint
    // offering both modes lands on a single row.
    const byName = new Map();
    for (const mode of MODES) {
      for (const name of Object.keys(namespace[mode] ?? {})) {
        const entry = byName.get(name) ?? { package: manifest.name, name };
        entry[mode] = `constraints.${mode}.${name}`;
        byName.set(name, entry);
      }
    }
    entries.push(...byName.values());
  }
  return entries;
}

function ref(value) {
  return value === undefined ? "—" : `\`${value}\``;
}

function render(entries) {
  const rows = entries
    .sort((a, b) => a.package.localeCompare(b.package) || a.name.localeCompare(b.name))
    .map((e) => `| \`${e.package}\` | \`${e.name}\` | ${ref(e.validate)} | ${ref(e.sanitize)} |`);

  return [
    "<!-- GENERATED by scripts/gen-constraint-catalogue.mjs — do not edit. Run `npm run catalogue`. -->",
    "",
    "# AWS-property constraint catalogue",
    "",
    "Every entry below validates an AWS-property string at synth time, so a malformed value",
    "fails in `cdk synth` at the authoring call site instead of at CloudFormation deploy time.",
    "The mechanism lives in `@composurecdk/cloudformation`; per-resource entries live in the",
    "package that owns the builder. See [ADR-0010](adr/0010-aws-property-constraints.md).",
    "",
    "Each package exposes its constraints through a uniform `constraints` namespace, so you",
    "import only the package you already use — e.g.",
    '`import { constraints } from "@composurecdk/ec2"`, then',
    "`constraints.validate.securityGroupDescription(value)`. The `validate*` functions throw;",
    "the `sanitize*` functions return a cleaned copy. Each thrown error names the allowed",
    "character set and links the relevant AWS doc.",
    "",
    "Cross-cutting tag key/value validation is not listed here; it is applied automatically by",
    "`taggedBuilder` and reachable directly via `validateTag` in `@composurecdk/cloudformation`.",
    "",
    "| Package | Constraint | Validate | Sanitize |",
    "| --- | --- | --- | --- |",
    ...rows,
    "",
  ].join("\n");
}

async function main() {
  const check = process.argv.includes("--check");
  const entries = await discover();
  const config = await resolveConfig(OUTPUT);
  const content = await format(render(entries), { ...config, parser: "markdown" });

  if (check) {
    const current = existsSync(OUTPUT) ? readFileSync(OUTPUT, "utf8") : "";
    if (current !== content) {
      console.error(
        `docs/constraints.md is out of date (${String(entries.length)} constraints found). ` +
          "Run `npm run catalogue` and commit the result.",
      );
      process.exit(1);
    }
    console.log(`docs/constraints.md is up to date (${String(entries.length)} constraints).`);
    return;
  }

  writeFileSync(OUTPUT, content);
  console.log(`Wrote docs/constraints.md (${String(entries.length)} constraints).`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
