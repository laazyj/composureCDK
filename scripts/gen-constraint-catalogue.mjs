#!/usr/bin/env node

/**
 * Generates `docs/constraints.md` — the single browsable index of every
 * AWS-property constraint in the library (see ADR-0009).
 *
 * Discoverability is a convention, not a runtime aggregate: each builder
 * package keeps its own constraints, so there is no single importable object
 * listing them all. This script provides the browse-everything view instead by
 * importing each published package's built output and collecting every exported
 * `StringConstraint` (anything carrying `pattern` / `name` / `allowed` /
 * `source`). Because it reads `dist`, run `npm run build` first.
 *
 * - default: write `docs/constraints.md`.
 * - `--check`: regenerate in memory and exit non-zero if the committed file is
 *   stale. Wired into `npm run verify` so a new constraint can't merge without
 *   its catalogue row.
 *
 * Usage:
 *   node scripts/gen-constraint-catalogue.mjs
 *   node scripts/gen-constraint-catalogue.mjs --check
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

import { format, resolveConfig } from "prettier";

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const PACKAGES_DIR = join(REPO_ROOT, "packages");
const OUTPUT = join(REPO_ROOT, "docs", "constraints.md");

function isStringConstraint(value) {
  return (
    typeof value === "object" &&
    value !== null &&
    value.pattern instanceof RegExp &&
    typeof value.name === "string" &&
    typeof value.allowed === "string" &&
    typeof value.source === "string"
  );
}

async function discover() {
  const entries = [];
  for (const dir of readdirSync(PACKAGES_DIR).sort()) {
    const manifestPath = join(PACKAGES_DIR, dir, "package.json");
    if (!existsSync(manifestPath)) continue;
    const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
    if (manifest.private) continue;

    const distIndex = join(PACKAGES_DIR, dir, "dist", "esm", "index.js");
    if (!existsSync(distIndex)) {
      throw new Error(
        `Missing build output for ${manifest.name} (${distIndex}). Run \`npm run build\` first.`,
      );
    }

    const module = await import(pathToFileURL(distIndex).href);
    for (const [exportName, value] of Object.entries(module)) {
      if (isStringConstraint(value)) {
        entries.push({ package: manifest.name, exportName, constraint: value });
      }
    }
  }
  return entries;
}

function limits({ minLength, maxLength }) {
  if (minLength !== undefined && maxLength !== undefined) return `${minLength}–${maxLength}`;
  if (maxLength !== undefined) return `≤ ${maxLength}`;
  if (minLength !== undefined) return `≥ ${minLength}`;
  return "—";
}

function cell(text) {
  return text.replaceAll("|", "\\|");
}

function render(entries) {
  const rows = entries
    .sort(
      (a, b) =>
        a.package.localeCompare(b.package) || a.constraint.name.localeCompare(b.constraint.name),
    )
    .map(
      (e) =>
        `| ${cell(e.constraint.name)} | \`${e.package}\` | \`${e.exportName}\` | ${limits(e.constraint)} | \`${cell(e.constraint.allowed)}\` | [ref](${e.constraint.source}) |`,
    );

  return [
    "<!-- GENERATED by scripts/gen-constraint-catalogue.mjs — do not edit. Run `npm run catalogue`. -->",
    "",
    "# AWS-property constraint catalogue",
    "",
    "Every entry below validates an AWS-property string at synth time, so a malformed value",
    "fails in `cdk synth` at the authoring call site instead of at CloudFormation deploy time.",
    "The mechanism lives in `@composurecdk/cloudformation`; per-resource entries live in the",
    "package that owns the builder. See [ADR-0009](adr/0009-aws-property-constraints.md).",
    "",
    "Call a constraint's validator from the package that owns it — e.g.",
    '`import { validateSecurityGroupDescription } from "@composurecdk/ec2"`, or',
    "`pkg.constraints.validate.*` for the uniform per-package namespace. Importing a service",
    "package pulls in only that package, never the whole catalogue.",
    "",
    "| Constraint | Package | Constant | Length | Allowed | AWS reference |",
    "| --- | --- | --- | --- | --- | --- |",
    ...rows,
    "",
  ].join("\n");
}

async function main() {
  const check = process.argv.includes("--check");
  const entries = await discover();
  const config = await resolveConfig(OUTPUT);
  const content = await format(render(entries), { ...config, parser: "markdown" });

  if (check) {
    const current = existsSync(OUTPUT) ? readFileSync(OUTPUT, "utf8") : "";
    if (current !== content) {
      console.error(
        `docs/constraints.md is out of date (${String(entries.length)} constraints found). ` +
          "Run `npm run catalogue` and commit the result.",
      );
      process.exit(1);
    }
    console.log(`docs/constraints.md is up to date (${String(entries.length)} constraints).`);
    return;
  }

  writeFileSync(OUTPUT, content);
  console.log(`Wrote docs/constraints.md (${String(entries.length)} constraints).`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
